                    ])->withInput();
                }
            }

            foreach ($days as $dayId) {
                ShiftEmployee::updateOrCreate(
                    [
                        'employee_id' => $employee_id,
                        'shift_id' => $request->shift_id,
                        'day_id' => $dayId,
                        'status' => 1,
                    ],
                    [
                        'clinic_id' => $auth_app,
                        'account_type' => $clinic->app_type,
                        'dateA' => null,
                    ]
                );
            }

            return back()->with('success', $this->safeText('&#1578;&#1605;&#1578; &#1573;&#1590;&#1575;&#1601;&#1577; &#1575;&#1604;&#1588;&#1610;&#1601;&#1578; &#1576;&#1606;&#1580;&#1575;&#1581;', 'Shift added successfully'));
        }

        return back();
