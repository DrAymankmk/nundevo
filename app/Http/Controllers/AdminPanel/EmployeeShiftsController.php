                ->where('account_type', $clinic->app_type)
                ->findOrFail($request->shift_id);
            $days = array_values(array_unique($request->days));

            foreach ($days as $dayId) {
                $duplicate = ShiftEmployee::where('employee_id', $employee_id)
                    ->where('day_id', $dayId)
                    ->where('status', 1)
                    ->whereHas('shift', function ($query) use ($selectedShift) {
                        $query->where('time_from', $selectedShift->time_from)
                            ->where('time_to', $selectedShift->time_to);
                    })
                    ->exists();

                if ($duplicate) {
                    $day = Day::find($dayId);
                    $dayName = $day
                        ? (app()->getLocale() == 'ar' ? $day->name_ar : $day->name_en)
                        : $dayId;

