                    ])->withInput();
                }
            }

            foreach ($period as $date) {
                ShiftEmployee::updateOrCreate(
                    [
                        'employee_id' => $employee_id,
                        'status' => 0,
                        'dateA' => $date->format('Y-m-d'),
                    ],
