    private function prepareCalendarEvents($employeeShifts)
    {
        $events = [];
        $recurringShifts = [];

        foreach ($employeeShifts as $shift) {
            if ($shift->status == 1) {
                $timeRange = $this->formatShiftTimeRange($shift->shift);

                if ($shift->day_id) {
                    $shiftKey = implode('_', [
                        'shift',
                        $shift->shift_id,
                        optional($shift->shift)->time_from,
                        optional($shift->shift)->time_to,
                    ]);

                    if (!isset($recurringShifts[$shiftKey])) {
                        $recurringShifts[$shiftKey] = [
                            'id' => $shift->id,
                            'title' => $timeRange,
                            'daysOfWeek' => [],
                            'startTime' => optional($shift->shift)->time_from ?? '09:00',
                            'endTime' => optional($shift->shift)->time_to ?? '17:00',
                            'color' => '#3788d8',
                            'display' => 'block',
                            'extendedProps' => [
                                'type' => 'recurring_shift',
                                'shift_id' => $shift->shift_id,
                                'employee_id' => $shift->employee_id,
                            ],
                        ];
                    }

                    $recurringShifts[$shiftKey]['daysOfWeek'][] = $this->convertDayToFullCalendarFormat($shift->day_id);
                } elseif ($shift->dateA) {
                    $events[] = [
                        'id' => $shift->id,
                        'title' => $timeRange,
                        'start' => $shift->dateA . 'T' . (optional($shift->shift)->time_from ?? '09:00'),
                        'end' => $shift->dateA . 'T' . (optional($shift->shift)->time_to ?? '17:00'),
                        'color' => '#3788d8',
                        'extendedProps' => [
                            'type' => 'specific_shift',
                        ],
                    ];
                }
            } else {
                if ($shift->dateA) {
                    if (strpos($shift->dateA, ' - ') !== false) {
                        $dates = explode(' - ', $shift->dateA);
                        $startDate = Carbon::parse($dates[0]);
                        $endDate = Carbon::parse($dates[1]);

                        $events[] = [
                            'id' => $shift->id,
                            'title' => $this->safeText('&#1573;&#1580;&#1575;&#1586;&#1577;', 'Holiday'),
                            'start' => $startDate->format('Y-m-d'),
                            'end' => $endDate->copy()->addDay()->format('Y-m-d'),
                            'color' => '#f39c12',
                            'allDay' => true,
                            'extendedProps' => [
                                'type' => 'holiday',
                                'start_date' => $startDate->format('Y-m-d'),
                                'end_date' => $endDate->format('Y-m-d'),
                            ],
                        ];
                    } else {
                        $date = Carbon::parse($shift->dateA);
                        $events[] = [
                            'id' => $shift->id,
                            'title' => $this->safeText('&#1573;&#1580;&#1575;&#1586;&#1577;', 'Holiday'),
                            'start' => $date->format('Y-m-d'),
                            'end' => $date->copy()->addDay()->format('Y-m-d'),
                            'color' => '#f39c12',
                            'allDay' => true,
                            'extendedProps' => [
                                'type' => 'holiday',
                                'date' => $date->format('Y-m-d'),
                            ],
                        ];
                    }
                }
            }
        }

        foreach ($recurringShifts as $recurringShift) {
            $recurringShift['daysOfWeek'] = array_values(array_unique($recurringShift['daysOfWeek']));
            $events[] = $recurringShift;
        }

        return $events;
    }
    private function formatShiftTimeRange($shift)
    {
        $timeFrom = optional($shift)->time_from
            ? Carbon::parse($shift->time_from)->format('H:i')
            : '09:00';

        $timeTo = optional($shift)->time_to
            ? Carbon::parse($shift->time_to)->format('H:i')
            : '17:00';

        return $this->safeText('&#1605;&#1606;', 'From') . ' ' . $timeFrom . ' ' . $this->safeText('&#1573;&#1604;&#1609;', 'To') . ' ' . $timeTo;
    }

    private function convertDayToFullCalendarFormat($dayId)
    {
        $day = Day::find($dayId);

        if ($day) {
            $dayName = strtolower(trim($day->name_en));
            $dayName = str_replace([' ', '_', '-'], '', $dayName);

            $mapByName = [
                'sunday' => 0,
                'sun' => 0,
                'monday' => 1,
                'mon' => 1,
                'tuesday' => 2,
                'tue' => 2,
                'tues' => 2,
                'wednesday' => 3,
                'wed' => 3,
                'thursday' => 4,
                'thu' => 4,
                'thurs' => 4,
                'friday' => 5,
                'fri' => 5,
                'saturday' => 6,
                'sat' => 6,
            ];

            if (array_key_exists($dayName, $mapByName)) {
                return $mapByName[$dayName];
            }
        }

        $map = [
            1 => 6,
            2 => 0,
            3 => 1,
            4 => 2,
            5 => 3,
            6 => 4,
            7 => 5,
        ];
        return $map[$dayId] ?? 0;
    }

    private function safeText($ar, $en)
    {
        return app()->getLocale() == 'ar'
            ? html_entity_decode($ar, ENT_QUOTES, 'UTF-8')
            : $en;
    }
